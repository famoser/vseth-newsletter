namespace App\Entity\Traits;

trait PriorityTrait
{
    /**
     * @var int
     *
     * @ORM\Column(type="integer")
     */
    private $priority;

    public function getPriority(): int
    {
        return $this->priority;
    }

    public function setPriority(int $priority): void
    {
        $this->priority = $priority;
    }
}
